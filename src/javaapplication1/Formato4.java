    private boolean comprueba(){
        boolean com = false;
        
        if ( !this.txfNombreDocumentador.getText().isEmpty() && !this.txfNombreDocumentador.getText().isEmpty() && 
                !this.txfDataChofer.getText().isEmpty() && !this.txfDataTransporte.getText().isEmpty() && 
                !this.txfNRemision.getText().isEmpty() && !this.txfNombreVigilante.getText().isEmpty() && 
                !this.txfNumEnbarque.getText().isEmpty()){
            
            com = true;
        } 
        
        return com;
    }
    
    private boolean filallena(JTable tb){
        boolean va = true; 
        int r = tb.getRowCount();
        for (int i = 1; i < tb.getColumnCount(); i++){
            
            if ( tb.getValueAt(r -1, i) == null) {
                va = false;
                break;
            }else
                System.out.println(tb.getValueAt(r -1, i));      
        }
        return va;
    }
    
