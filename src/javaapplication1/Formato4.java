    private void btnEliminarFilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarFilaActionPerformed
        // TODO add your handling code here:
        if (tabla.getRowCount() > 0 ){
            if (filallena(jtRMR)){ 
                int confirmar = JOptionPane.showConfirmDialog(null,
                "Esta seguro que desea Eliminar el registro? ");            
            if (JOptionPane.OK_OPTION == confirmar){
                Object valueAt = tabla.getValueAt(jtRMR.getSelectedRow(), 6);
                volTotal = volTotal - Double.parseDouble(valueAt.toString());
                lbVolTotal.setText( decimales.format(volTotal) );
                int selectedRow = jtRMR.getSelectedRow();
                tabla.removeRow(selectedRow);     
                for (int i= 0; i < tabla.getRowCount(); i++){
                    jtRMR.setValueAt(i + 1, i, 0);
                }
            }               
            }else
                JOptionPane.showMessageDialog(null, "Termine de llenar la fila");
           }else
            JOptionPane.showMessageDialog(null, "Agregre una fila primero!");
    }//GEN-LAST:event_btnEliminarFilaActionPerformed

