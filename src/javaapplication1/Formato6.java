    private boolean comprueba(){
        boolean com = false;      
        if ( !this.txfComunidad.getText().isEmpty() && !this.txfResponsable.getText().isEmpty() ){  
            com = true;
        }        
        return com;
    }
    
    private void bloqueo(){
        this.txfComunidad.setEditable(false);
        this.txfResponsable.setEditable(false);
        this.jcbGenero.setEnabled(false);
        this.jcbProcedencia.setEnabled(false);
        this.dcFechaIngreso.setEnabled(false);
        this.jsHoraEntrada.setEnabled(false);
    }
    
