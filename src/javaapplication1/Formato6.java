     private boolean filallena(JTable tb){
        boolean va = true;       
        int r = tb.getRowCount();        
        for (int i = 1; i < tb.getColumnCount(); i++){           
            if ( tb.getValueAt(r -1, i) == null) {
                va = false;
                break;
            }else
                System.out.println(tb.getValueAt(r -1, i));      
        }       
        return va;
    }
     
     private void liberar(){
        this.txfComunidad.setEditable(true);
        this.txfResponsable.setEditable(true);
        this.jcbGenero.setEnabled(true);
        this.jcbProcedencia.setEnabled(true);
        this.dcFechaIngreso.setEnabled(true);
        this.jsHoraEntrada.setEnabled(true);
     }
     
     private void limpiar(){
        this.txfComunidad.setText("");
        this.txfResponsable.setText("");
        volTotal = 0.0;
        lbVolTotal.setText( decimales.format(volTotal) );
                
        tabla.setRowCount(0);
        jtRMR.setModel(new DefaultTableModel());
        tabla = (DefaultTableModel) jtRMR.getModel();
        
     }
